//This example shows how to use variables 
public class JavaExample2 {

	public static void main(String[] args) {
		
		//declaring an variable integer
		int ourNumber;
		//assigning a value to our variable integer
		ourNumber = 0;
		//prints out 0
		System.out.println(ourNumber);
		
		ourNumber = 10;
		System.out.println(ourNumber);
		
		double decimalNumber;
		decimalNumber = 10.15;
		System.out.println(decimalNumber);
		
		
		
	}

}
